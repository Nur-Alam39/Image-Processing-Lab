INCLUDE EMU8086.INC
.MODEL SMALL
.STACK 100H
.CODE
MAIN PROC
 PRINT "Enter first number:"
 MOV AH,1
 INT 21H
 MOV BL,AL
 PRINTN
 PRINT "Enter second number:"
 MOV AH,1
 INT 21H 
 MOV CL,AL
 ADD BL,CL
 PRINTN
 PRINT "Sum of the numbers:"
 MOV AH,2
 SUB BL, 48
 MOV DL,BL 
 INT 21H
 MOV AH, 4CH
 INT 21H
 MAIN ENDP
END MAIN